    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $this->validate($request,['identification_card'=>'required', 'name'=>'required', 'last_name'=>'required', 'gender'=>'required', 'birthdate'=>'required', 'direction'=>'required']);
        Doctor::create($request->all());
        return redirect()->route('Doctor.index')->with('success','Registro creado satisfactoriamente');

    }

